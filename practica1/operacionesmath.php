<?php
// 1. Suma (+)
$num1 = 5;
$num2 = 3;
$suma = $num1 + $num2;
echo "1. Suma: $num1 + $num2 = $suma\n";
echo "<br>";// 2. Resta (-)
$num1 = 10;
$num2 = 4;
$resta = $num1 - $num2;
echo "2. Resta: $num1 - $num2 = $resta\n";
echo "<br>";// 3. Multiplicación (*)
$num1 = 7;
$num2 = 3;
$mult = $num1 * $num2;
echo "3. Multiplicación: $num1 * $num2 = $mult\n";
echo "<br>";// 4. División (/)
$num1 = 15;
$num2 = 5;
$div = $num1 / $num2;
echo "4. División: $num1 / $num2 = $div\n";
echo "<br>";    // 5. Módulo (%) -> El resto de la división
$num1 = 10;
$num2 = 3;
$mod = $num1 % $num2;
